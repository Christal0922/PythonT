# 常用快捷键
# 运行当前代码： ctrl+shift+F10
# 运行当前脚本： shift+F10
# 查找： ctrl+F
# 全局查找： ctrl+shift+F
# 替换： ctrl+R
# 全局替换： ctrl+shift+R
# 缩进：tab
# 反向缩进：shift+tab
# 行首： HOME
# 行尾： END
# 打开settings: ctrl+alt+S

# 快速修正： alt+enter
# 快速注释： ctrl+/
# 复制代码： ctrl+D
# 删除代码： ctrl+Y
# 复写代码： ctrl+O
# 选中单词或代码块： ctrl+W
# 快速查看文档： ctrl+Q
# 模块或项目重命名：shift+F6
# 向下插入： shift+enter
# 向上插入：ctrl+alt+enter

# 查看项目视图：alt+1
# 查看结构视图：alt+7
# 展开代码：ctrl+shift++
# 折叠所有代码： ctrl+shift+-
# 转到方法定义处：Ctrl+B/鼠标左键
# 快速查看历史：alt+左右方向
# 快速切换方法：alt+上下
# 切换视图：ctrl+tab
# 查看资源文件：shift+shift
# 查看方法在哪里被调用： ctrl+alt+H
# 查看父类：ctrl+U
# 查看继承关系：ctrl+H
